namespace PW;

/// <summary>
/// Specifies the direction of a sort operation.
/// </summary>
public enum SortOrder
{
  /// <summary>
  /// Sort Ascending
  /// </summary>
  Ascending = 1,

  /// <summary>
  /// Sort Descending
  /// </summary>
  Descending = -1,

  /// <summary>
  /// Sort direction unspecified
  /// </summary>
  Unspecified = 0
}
